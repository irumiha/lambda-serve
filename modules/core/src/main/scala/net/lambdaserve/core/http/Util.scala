    case SwitchingProtocols          extends HttpStatus(101)
    case Processing                  extends HttpStatus(102)
    case EarlyHints                  extends HttpStatus(103)
    case OK                          extends HttpStatus(200)
    case Created                     extends HttpStatus(201)
    case Accepted                    extends HttpStatus(202)
    case NonAuthoritativeInformation extends HttpStatus(203)
    case NoContent                   extends HttpStatus(204)
    case ResetContent                extends HttpStatus(205)
    case PartialContent              extends HttpStatus(206)
    case MultiStatus                 extends HttpStatus(207)
    case AlreadyReported             extends HttpStatus(208)
    case IMUsed                      extends HttpStatus(226)
    case MultipleChoices             extends HttpStatus(300)
    case MovedPermanently            extends HttpStatus(301)
    case Found                       extends HttpStatus(302)
    case SeeOther                    extends HttpStatus(303)
    case NotModified                 extends HttpStatus(304)
    case UseProxy                    extends HttpStatus(305)
    case TemporaryRedirect           extends HttpStatus(307)
    case PermanentRedirect           extends HttpStatus(308)
    case BadRequest                  extends HttpStatus(400)
    case Unauthorized                extends HttpStatus(401)
    case PaymentRequired             extends HttpStatus(402)
    case Forbidden                   extends HttpStatus(403)
    case NotFound                    extends HttpStatus(404)
    case MethodNotAllowed            extends HttpStatus(405)
    case NotAcceptable               extends HttpStatus(406)
    case ProxyAuthenticationRequired extends HttpStatus(407)
    case RequestTimeout              extends HttpStatus(408)
    case Conflict                    extends HttpStatus(409)
    case Gone                        extends HttpStatus(410)
    case LengthRequired              extends HttpStatus(411)
    case PreconditionFailed          extends HttpStatus(412)
    case PayloadTooLarge             extends HttpStatus(413)
    case URITooLong                  extends HttpStatus(414)
    case UnsupportedMediaType        extends HttpStatus(415)
    case RangeNotSatisfiable         extends HttpStatus(416)
    case ExpectationFailed           extends HttpStatus(417)
    case ImATeapot                   extends HttpStatus(418)
    case MisdirectedRequest          extends HttpStatus(421)
    case UnprocessableEntity         extends HttpStatus(422)
    case Locked                      extends HttpStatus(423)
    case FailedDependency            extends HttpStatus(424)
    case TooEarly                    extends HttpStatus(425)
    case UpgradeRequired             extends HttpStatus(426)
    case PreconditionRequired        extends HttpStatus(428)
    case TooManyRequests             extends HttpStatus(429)
    case RequestHeaderFieldsTooLarge extends HttpStatus(431)
    case UnavailableForLegalReasons  extends HttpStatus(451)
    case InternalServerError         extends HttpStatus(500)
    case NotImplemented              extends HttpStatus(501)
    case BadGateway                  extends HttpStatus(502)

  enum HttpHeader(val name: String):
    case Accept                        extends HttpHeader("Accept")
    case AcceptCharset                 extends HttpHeader("Accept-Charset")
    case AcceptEncoding                extends HttpHeader("Accept-Encoding")
    case AcceptLanguage                extends HttpHeader("Accept-Language")
    case AcceptRanges                  extends HttpHeader("Accept-Ranges")
    case AccessControlAllowCredentials extends HttpHeader("Access-Control-Allow-Credentials")
    case AccessControlAllowHeaders     extends HttpHeader("Access-Control-Allow-Headers")
    case AccessControlAllowMethods     extends HttpHeader("Access-Control-Allow-Methods")
    case AccessControlAllowOrigin      extends HttpHeader("Access-Control-Allow-Origin")
    case AccessControlExposeHeaders    extends HttpHeader("Access-Control-Expose-Headers")
    case AccessControlMaxAge           extends HttpHeader("Access-Control-Max-Age")
    case AccessControlRequestHeaders   extends HttpHeader("Access-Control-Request-Headers")
    case AccessControlRequestMethod    extends HttpHeader("Access-Control-Request-Method")
    case Age                           extends HttpHeader("Age")
    case Allow                         extends HttpHeader("Allow")
    case Authorization                 extends HttpHeader("Authorization")
    case CacheControl                  extends HttpHeader("Cache-Control")
    case Connection                    extends HttpHeader("Connection")
    case ContentDisposition            extends HttpHeader("Content-Disposition")
    case ContentEncoding               extends HttpHeader("Content-Encoding")
    case ContentLanguage               extends HttpHeader("Content-Language")
    case ContentLength                 extends HttpHeader("Content-Length")
    case ContentLocation               extends HttpHeader("Content-Location")
    case ContentRange                  extends HttpHeader("Content-Range")
    case ContentType                   extends HttpHeader("Content-Type")
    case Cookie                        extends HttpHeader("Cookie")
    case Date                          extends HttpHeader("Date")
    case ETag                          extends HttpHeader("ETag")
    case Expect                        extends HttpHeader("Expect")
    case Expires                       extends HttpHeader("Expires")
    case Forwarded                     extends HttpHeader("Forwarded")
    case From                          extends HttpHeader("From")
    case Host                          extends HttpHeader("Host")
    case IfMatch                       extends HttpHeader("If-Match")
    case IfModifiedSince               extends HttpHeader("If-Modified-Since")
    case IfNoneMatch                   extends HttpHeader("If-None-Match")
    case IfRange                       extends HttpHeader("If-Range")
    case IfUnmodifiedSince             extends HttpHeader("If-Unmodified-Since")
    case LastModified                  extends HttpHeader("Last-Modified")
    case Link                          extends HttpHeader("Link")
    case Location                      extends HttpHeader("Location")
    case MaxForwards                   extends HttpHeader("Max-Forwards")
    case Origin                        extends HttpHeader("Origin")
    case Pragma                        extends HttpHeader("Pragma")
    case ProxyAuthenticate             extends HttpHeader("Proxy-Authenticate")
