    case Range                         extends HttpHeader("Range")
    case Referer                       extends HttpHeader("Referer")
    case RetryAfter                    extends HttpHeader("Retry-After")
    case Server                        extends HttpHeader("Server")
    case SetCookie                     extends HttpHeader("Set-Cookie")
    case StrictTransportSecurity       extends HttpHeader("Strict-Transport-Security")
    case TE                            extends HttpHeader("TE")
    case Trailer                       extends HttpHeader("Trailer")
    case TransferEncoding              extends HttpHeader("Transfer-Encoding")
    case Upgrade                       extends HttpHeader("Upgrade")
    case UserAgent                     extends HttpHeader("User-Agent")
    case Vary                          extends HttpHeader("Vary")
    case Via                           extends HttpHeader("Via")
    case Warning                       extends HttpHeader("Warning")
    case WWWAuthenticate               extends HttpHeader("WWW-Authenticate")
